# Реализация метода на основе ВКР:
# «Применение технологий машинного обучения для анализа рынка недвижимости», 2025 г.

def calculate_trend_score(data: list[float]) -> float:
    """
    Заглушка функции оценки значимости показателя.
    В дальнейшем предполагается развитие метода с использованием
    интеллектуальных алгоритмов анализа данных.
    """
    if not data:
        return 0.0
    return sum(data) / len(data)
